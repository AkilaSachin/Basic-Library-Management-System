    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String sea=search.getText();
        if(risbn.isSelected()){          
            Integer i=new Integer(sea);
            Bookclass boo=library.getlibrary().getHoldings().findbookfromISBN(i);
            DefaultTableModel model=(DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);
            
                model.addRow(new Object[]{boo.getacc(),boo.gettitle(),boo.getauth(),boo.getISBN()});
            
        }
        else if(raccess.isSelected()){
            Integer i=new Integer(sea);
            Bookclass boo=library.getlibrary().getHoldings().findbookfromaccnum(i);
            DefaultTableModel model=(DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);
            
                model.addRow(new Object[]{boo.getacc(),boo.gettitle(),boo.getauth(),boo.getISBN()});
        }
        else if(rauthor.isSelected()){
            
            Bookclass boo=library.getlibrary().getHoldings().findbookbyauthor(sea);
            DefaultTableModel model=(DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);
            
                model.addRow(new Object[]{boo.getacc(),boo.gettitle(),boo.getauth(),boo.getISBN()});
        }
        else if(rtitle.isSelected()){
            
            Bookclass boo=library.getlibrary().getHoldings().findbookbytitle(sea);
            DefaultTableModel model=(DefaultTableModel) jTable1.getModel();
            model.setRowCount(0);
            
                model.addRow(new Object[]{boo.getacc(),boo.gettitle(),boo.getauth(),boo.getISBN()});
        }
        else{
            JOptionPane.showMessageDialog(rootPane, "Please Select Search Method");
            tableload();
        }
    }//GEN-LAST:event_jButton1ActionPerformed

