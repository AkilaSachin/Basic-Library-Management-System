    public void tableload(){
        library.read("book.txt");
        Bookclass [] bo=library.getlibrary().getHoldings().getallbooks();
        DefaultTableModel model=(DefaultTableModel) jTable1.getModel();
        model.setRowCount(0);
        for(Bookclass book:bo){
            
            model.addRow(new Object[]{book.getacc(),book.gettitle(),book.getauth(),book.getISBN(),book.getAvailability()});
        }
        int c=jTable1.getRowCount();
        int y=(int) jTable1.getValueAt(c-1, 0);
        int ac=y+1;
        accesion.setText(Integer.toString(ac));
    }
